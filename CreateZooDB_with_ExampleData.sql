-- ตารางประเภทสัตว์ป่า
CREATE TABLE AnimalType (
    animal_type_id INT PRIMARY KEY IDENTITY(1,1),
    type_name NVARCHAR(100) NOT NULL,
    description NVARCHAR(MAX)
);
GO

-- ตารางชนิดสัตว์
CREATE TABLE SpeciesInfo (
    species_info_id INT PRIMARY KEY IDENTITY(1,1),
    common_name NVARCHAR(100) NOT NULL,
    scientific_name NVARCHAR(100),
    habitat NVARCHAR(MAX),
    diet NVARCHAR(MAX),
    conservation_status NVARCHAR(100),
    description NVARCHAR(MAX)
);
GO

-- ตารางกรงแสดงสัตว์
CREATE TABLE Enclosure (
    enclosure_id INT PRIMARY KEY IDENTITY(1,1),
    name NVARCHAR(100) NOT NULL,
    location NVARCHAR(200)
);
GO

-- ตารางผู้ดูแล
CREATE TABLE Keeper (
    keeper_id INT PRIMARY KEY IDENTITY(1,1),
    name NVARCHAR(100) NOT NULL,
    phone NVARCHAR(50),
    email NVARCHAR(100)
);
GO

-- ตารางกลางเชื่อมกรงกับผู้ดูแล (Many-to-Many)
CREATE TABLE EnclosureKeeper (
    enclosure_id INT,
    keeper_id INT,
    PRIMARY KEY (enclosure_id, keeper_id),
    FOREIGN KEY (enclosure_id) REFERENCES Enclosure(enclosure_id),
    FOREIGN KEY (keeper_id) REFERENCES Keeper(keeper_id)
);
GO

-- ตารางอาหารสัตว์
CREATE TABLE Food (
    food_id INT PRIMARY KEY IDENTITY(1,1),
    name NVARCHAR(100) NOT NULL,
    type NVARCHAR(100),
    unit NVARCHAR(50)
);
GO

-- ตารางสัตว์
CREATE TABLE Animal (
    animal_id INT PRIMARY KEY IDENTITY(1,1),
    name NVARCHAR(100) NOT NULL,
    gender NVARCHAR(10),
    birth_date DATE,
    enclosure_id INT,
    animal_type_id INT,
    species_info_id INT,
    FOREIGN KEY (enclosure_id) REFERENCES Enclosure(enclosure_id),
    FOREIGN KEY (animal_type_id) REFERENCES AnimalType(animal_type_id),
    FOREIGN KEY (species_info_id) REFERENCES SpeciesInfo(species_info_id)
);
GO

-- ตารางการให้อาหารสัตว์รายวัน
CREATE TABLE FeedingSchedule (
    feeding_id INT PRIMARY KEY IDENTITY(1,1),
    animal_id INT NOT NULL,
    food_id INT NOT NULL,
    amount DECIMAL(10,2),
    feeding_date DATE,
    feeding_time TIME,
    keeper_id INT,
    FOREIGN KEY (animal_id) REFERENCES Animal(animal_id),
    FOREIGN KEY (food_id) REFERENCES Food(food_id),
    FOREIGN KEY (keeper_id) REFERENCES Keeper(keeper_id)
);
GO
